  /**
   * 地域に応じた標準的な土地面積を取得
   * @param prefecture 都道府県名
   * @param city 市区町村名
   * @returns 土地面積（坪）
   */
  export function getLandSize(prefecture: string, city: string): number {
    // 1. 市区町村レベルでチェック
    const municipalityConfig = LAND_SIZE_CONFIG.municipalities[prefecture];
    if (municipalityConfig && municipalityConfig[city]) {
      return municipalityConfig[city];
    }
    
    // 2. 都道府県レベルでチェック
    const prefectureSize = LAND_SIZE_CONFIG.prefectures[prefecture];
    if (prefectureSize) {
      return prefectureSize;
    }
    
    // 3. デフォルト値を返す
    return LAND_SIZE_CONFIG.default;
  }
  
  /**
   * デバッグ用：設定されている土地面積を確認
   */
  export function debugLandSize(prefecture: string, city: string): string {
    const size = getLandSize(prefecture, city);
    const reason = LAND_SIZE_CONFIG.municipalities[prefecture]?.[city] 
      ? '市区町村個別設定' 
      : LAND_SIZE_CONFIG.prefectures[prefecture]
      ? '都道府県設定'
      : 'デフォルト設定';
    
    return `${prefecture}${city}: ${size}坪 (${reason})`;
  }